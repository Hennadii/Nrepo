namespace Nrepo
{
	/// <summary>
	/// Represents the sorting directions of resulting queries.
	/// </summary>
	public enum SortingType
	{
		/// <summary>
		/// Represents no sorting.
		/// </summary>
		None = 0,

		/// <summary>
		/// Represents ascending sorting.
		/// </summary>
		Ascending = 1,

		/// <summary>
		/// Represents descending sorting.
		/// </summary>
		Descending = 2
	}
}
